<script>
    $('#noteStoreBtn').click(function(e) {
        e.preventDefault();
        var data = new FormData($('#noteStore')[0]);
        var url = $('#noteStore').attr('action');
        var type = $('#noteStore').attr('method');
        var csrf_token = $("meta[name='csrf_token']").attr("content");
        $.ajax({
            url: url,
            data: data,
            cache: false,
            contentType: false,
            processData: false,
            type: type,
            headers: {
                'X-CSRF-TOKEN': csrf_token
            },
            success: function(response) {
                if (response.result == true) {
                    $('#noteStore').addClass('d-none');
                    $('#noteResultsuccess').removeClass('d-none');
                    Toast.fire({
                        icon: 'success',
                        title: response?.message
                    });
                }
            },
            error: function(response) {
              $('#noteStore').addClass('d-none');
              $('#noteResulterror').removeClass('d-none');
                console.log(response);
            }
        });
    });
</script>
